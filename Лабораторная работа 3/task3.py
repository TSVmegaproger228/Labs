def count_letters(text):
    symbols = set(text.lower())
    letters = {}
    for char in symbols:
        counter = 0
        if char.isalpha():
            for second_char in text:
                if second_char == char:
                    counter += 1
            letters[char] = counter
    return letters


# TODO Напишите функцию calculate_frequency
def calculate_frequency(letters):
    total = sum(letters.values())
    for key in letters.keys():
        letters[key] = letters.get(key) / total
    return letters


